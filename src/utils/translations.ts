// Función para traducir tipo
export const translateType = (type: string): string => {
  return typeTranslations[type.toLowerCase()] || type;
};

// Función para traducir estadística
export const translateStat = (stat: string): string => {
  return statTranslations[stat.toLowerCase()] || stat;
};

// Función para traducir movimiento
export const translateMove = (move: string): string => {
  const normalizedMove = move.toLowerCase();
  return moveTranslations[normalizedMove] || move.replace(/-/g, ' ');
};
